export const mutations = {
  setProduct(state, payload) {
    state.productDetail = payload
  },
}

export const actions = {
  getProduct(context, category) {
    // eslint-disable-next-line no-console
    console.log(context.state.productList)
    const getCategory = context.state.productCategoryList.find((value) => {
      return value.name === category
    })
    const filterByCategory = context.state.productList.filter((value) => {
      return value.category === category
    })

    const responseObject = {
      ...getCategory,
      items: filterByCategory,
    }

    context.commit('setProduct', responseObject)
  },
}
